const CI = { A: 0, B: 1, C: 2, D: 3 }

const rows = questions.map(q => ({
  subject:          q.subject,
  topic:            q.topic,
  difficulty:       q.difficulty,
  stem:             q.stem,
  context_text:     q.context ?? null,
  options_json:     [q.options_json.A, q.options_json.B, q.options_json.C, q.options_json.D],
  correct_index:    CI[q.correct_index],
  explanation:      q.explanation,
  icfes_competency: q.icfes_competency ?? null,
}))

async function seed() {
  console.log(`\n🧮 Iniciando inserción de ${rows.length} preguntas de Matemáticas (Lote 1)...`)
  console.log('   Distribución: 60 fáciles + 40 medias')
  console.log('   Temas: Álgebra, Geometría, Estadística, Razonamiento Numérico,')
  console.log('          Cálculo, Trigonometría, Lógica, Matemática Financiera\n')

  const { data, error } = await supabase
    .from('questions')
    .insert(rows)
    .select('id')

  if (error) {
    console.error('❌ Error en inserción:', error.message)
    console.error('   Detalle:', error.details ?? 'sin detalles')
    process.exit(1)
  }

  console.log(`✅ ${data.length} preguntas insertadas exitosamente.`)
  console.log(`   IDs: ${data[0].id} ... ${data[data.length - 1].id}`)

  // Verificación post-inserción
  const { count } = await supabase
    .from('questions')
    .select('*', { count: 'exact', head: true })
    .eq('subject', 'matematicas')

  console.log(`\n📊 Total de preguntas de Matemáticas en BD: ${count}`)
}

